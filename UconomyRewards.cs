namespace Remastered.DeathMessages
{
    public class UconomyRewards
    {
        public int Head;
        public int Body;
        public int Arm;
        public int Leg;
        public int Roadkill;

        public UconomyRewards()
        {
        }

        public UconomyRewards(int head, int body, int arm, int leg, int roadkill)
        {
            Head = head;
            Body = body;
            Arm = arm;
            Leg = leg;
            Roadkill = roadkill;
        }
    }
}